// Model::load() automatically caches its compiled decode graph as a sibling file
// of model.safetensors (CLAUDE.md/TODO.md Phase 4) -- maxSequenceLength=10 here is
// unused by any other test in this file, so this test's cache file can't collide
// with (or be left over from) another test sharing writableFixtureDir().
TEST(Model, GraphCacheRoundTripProducesIdenticalOutput)
{
    constexpr std::int64_t maxSequenceLength = 10;
    std::string dir = writableFixtureDir();
    std::string cachePath = dir + "/campello_llm_decode_graph." + std::to_string(maxSequenceLength) + ".cache";
    std::filesystem::remove(cachePath);
    ASSERT_FALSE(std::filesystem::exists(cachePath));

    GenerationConfig config;
    config.maxTokens = 4;
    config.temperature = 0.0f;

    auto contextA = cnn::Context::create({cnn::DeviceType::Cpu});
    auto modelA = Model::load(contextA, dir, maxSequenceLength);
    // First load() found no cache, built fresh, and should have written one.
    EXPECT_TRUE(std::filesystem::exists(cachePath));
    std::string outputA = modelA->generate("hi", config);

    auto contextB = cnn::Context::create({cnn::DeviceType::Cpu});
    auto modelB = Model::load(contextB, dir, maxSequenceLength); // should load from the cache written above
    std::string outputB = modelB->generate("hi", config);

    EXPECT_EQ(outputA, outputB);
}

TEST(Model, CorruptGraphCacheFallsBackToBuildingFresh)
{
    constexpr std::int64_t maxSequenceLength = 11; // dedicated, unused by other tests
    std::string dir = writableFixtureDir();
    std::string cachePath = dir + "/campello_llm_decode_graph." + std::to_string(maxSequenceLength) + ".cache";

    {
        std::ofstream out(cachePath, std::ios::binary);
        out << "not a valid campello_nn graph cache";
    }
    // Make sure the freshness check (cache mtime >= weights/config mtime) doesn't
    // skip this cache for the wrong reason -- it must be exercised, not skipped.
    std::filesystem::last_write_time(cachePath, std::filesystem::file_time_type::clock::now());

    auto context = cnn::Context::create({cnn::DeviceType::Cpu});
    std::unique_ptr<Model> model;
    EXPECT_NO_THROW(model = Model::load(context, dir, maxSequenceLength));

    GenerationConfig config;
    config.maxTokens = 3;
    config.temperature = 0.0f;
    std::string generated;
    EXPECT_NO_THROW(generated = model->generate("hi", config));
    (void)generated;
}
